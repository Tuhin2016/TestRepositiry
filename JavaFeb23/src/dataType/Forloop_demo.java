package dataType;

public class Forloop_demo {

	public static void main(String[] args) {
		
		
		/**
		 *  for(initialization/start point,how far/end point,how far end point,
		 *  {
		 *  code to be executed
		 *  }
		 */
		// write "i love you" 10 time using for loop
		System.out.println("starts");
		for(int i = 1; i<=10; i++) {
			System.out.println(i + ".i love you");
		}

	}

}
